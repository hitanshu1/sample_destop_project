
/// sell Gift card Model
class GiftCard{
  /// card Number
  String? cardNo;
  /// gift card value
  double ?value;
  /// gift card balance
  double ?balance;
  /// customer ID
  String ?customerName;
  /// expiry date
  String ?expiryDate;
/// constructor
  GiftCard({this.cardNo,this.value,this.balance,this.customerName,this.expiryDate});
}
